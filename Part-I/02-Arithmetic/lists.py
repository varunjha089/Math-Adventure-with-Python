"""
Operating on Lists
"""


def add(lists, app):
    return lists.append(app)


def sum_of_two_lists(lis1, lis2):
    return lis1 + lis2


def mul_of_two_lists(lis1, lis2):
    return lis1 * lis2


def remove_from_lists(lis, sub):
    return lis.remove(sub)


def access_individual_item(lis, index):
    return lis[index]


def access_range_in_lists(lis, range1, range2):
    return lis[range1:range2]


def find_the_index(lis, item):
    return lis.index(item)


